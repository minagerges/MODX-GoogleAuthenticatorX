$s = array(
    'gax_disabled' => true,
    'gax_courtesy_enabled' => true,
    'gax_profile_enabled' => false,
    'gax_issuer' => '',
    'gax_remember_period' => '30',
);

$settings = array();

foreach ($s as $key => $value) {
    if (is_string($value) || is_int($value)) { $type = 'textfield'; }
    elseif (is_bool($value)) { $type = 'combo-boolean'; }
    else { $type = 'textfield'; }
    $parts = explode('.',$key);
    if (count($parts) == 1) { $area = 'Default'; }
    else { $area = $parts[0]; }
    
    $settings[$key] = $modx->newObject('modSystemSetting');
    $settings[$key]->set('key', $key);
    $settings[$key]->fromArray(array(
        'value' => $value,
        'xtype' => $type,
        'namespace' => 'GoogleAuthenticatorX',
        'area' => $area
    ));
}
return $settings;